/** 
 * _____________REGRAS PARA DECLARAÇÃO DE VARIÁVEIS_____________
 * » Não Podem Ser Iniciadas Com Números, Apenas Com Letras || "_"
 * » Não Podem Ser Utilizados Caracteres Especias Como "Ç", "^", "~"
 * » Não Podem Ser Iguais Às Palavras Reservadas Da Linguagem
 * » JavaScript É Uma Linguagem Case Sensitive, var Ola != De var ola
 * 
 */

/** 
 * _____________TIPOS DE VARIÁVEIS_____________
 * var » Declara Uma Variável De Função E Pode Ser Reatribuída
 * let » Declara Uma Variável De Escopo De Bloco E Pode Ser Reatribuída
 * const » Declara Uma Variável Com Escopo De Bloco E Valor Constante E Não Pode Ser Reatribuída
 * 
*/

/** 
 * _____________TIPOS DE DADOS PRIMITIVOS_____________
 * string » Armazenam Cadeias De Caracteres
 * number » Armazenam Valores Numericos Positivos || Negativos || Int / Float
 * boolean » Armazenam Tipos De Estados True || False
 * null » Representa A Ausência Intencional De Um Valor
 * undefined » Representa Que A Variável Não Contem Nenhum Valor
 * 
 */

/** 
 * _____________ESTRUTURA DE CONTROLE_____________
 * if (Condição) » Bloco De Código Executado Se A Condição For Verdadeira
 * else if (Condição) » Bloco De Código Executado Se A Primeira Condição For Falsa E A Segunda For Verdadeira
 * else » Bloco De Código Executado Se Nenhuma Das Condições Anteriores For Verdadeira
 * 
 */

/** 
 * _____________OPERADORES DE COMPARAÇÃO_____________
 * == (Igual) » Verifica Se OS Valores Comparados São Iguais
 * === (Idêntico) » Verifica Se OS Valores Comparados São Iguais E Do Mesmo Tipo
 * != (Diferente) » Verifica Se OS Valores Comparados São Diferentes
 * !== (Não Idêntico) » Verifica Se OS Valores Comparados São Diferentes E De Tipos Diferentes
 * < (Menor) » Verifica Se OS Valores Da Esquerda É Menor Que O Valor Da Direita 
 * > (Maior) » Verifica Se OS Valores Da Esquerda É Maior Que O Valor Da Direita
 * <= (Menor Igual) » Verifica Se OS Valores Da Esquerda É Menor Ou Igual Ao Valor Da Direita
 * >= (Maior Igual) » Verifica Se OS Valores Da Esquerda É Maior Ou Igual Ao Valor Da Direita
 * 
 */

/** 
 * _____________OPERADORES LÓGICOS_____________
 * && (E) » Verdadeiro Se Todas As Expressões Forem Verdadeiras
 * || (OU) » Verdadeiro Se Pelo Menos Uma Das Expressões Forem Verdadeiras
 * ! (Negação) » Inverte O Resultado Da Expressão De Comparação
 * 
 */

/** 
 * _____________OPERADORES ARITMÉTICOS_____________
 * + (Adição) » Soma Valores
 * - (Subtração) » Diferença Entre Valores
 * * (Multiplicação) » Produto Dos Valores
 * / (Divisão) » Quociente Dos Valores
 * % (Módulo) » Resto Existente Em Uma Operação De Divisão
 * ++ (Incremento) » Pré/Pós Incremento
 * -- (Decremento) » Pré/Pós Decremento
 * 
 */

/** 
 * _____________EVENTOS_____________
 * »____________MOUSE____________
 * onclick » Quando O Elemento É Clicado
 * ondblclick » Quando O Elemento É Clicado Duas Vezes Rapidamente
 * onmouseup » Quando Um Determinado Elemento É Liberado
 * onmouseover » Quando O Cursor Passa Por Cima Do Elemento
 * onmouseout » Quando O Cursor Sair De Cima Do Elemento
 * »___________TECLADO___________
 * onkeydown » Quando Qualquer Tecla É Pressionada
 * onkeypress » Quando Qualquer Tecla É Mantida Pressionada
 * onkeyup » Quando A Tecla É Solta Após Ser Pressionada
 * »____________JANELA___________
 * onresize» Quando A Pagina ou Frame é Reposicionado
 * onscroll » Quando A Um Rolamento Da Pagina Ou Elemento
 * »_________FORMULÁRIO__________
 * onfocus » Quando O Elemento Recebe O Foco Do Cursor Do Mouse
 * onblur » Quando O Elemento Perde O Foco Do Cursor Do Mouse
 * onchange » Quando O Estado Do Elemento For Modificado
 * 
 */

/** 
 * _____________BOM_____________
 * »____________WINDOW____________
 *  window.innerWidth » Largura Interna Da Janela Do Navegador(Em Pixels)
 *  window.innerHeight » Altura Interna Da Janela Do Navegador(Em Pixels)
 *  window.open() » Abre Uma Nova Janela
 *  window.close() » Fecha A Janela Atual
 *  window.moveTo() » Move A Janela Atual
 *  window.resizeTo() » Redimensiona A Janela Atual
 *  window.alert() » Exibe Uma Janela De Alerta
 *  window.confirm() » Exibe Uma Caixa De Confirmação (OK / Cancelar)
 *  window.prompt() » Exibe Uma Caixa De Entrada Para O Usuário Digitar Algo
 * »____________SCREEN____________
 *  screen.width » Largura Da Tela Do Dispositivo
 *  screen.height » Altura Da Tela Do Dispositivo
 *  screen.availWidth » Largura Disponível (Sem A Barra De Tarefas)
 *  screen.availHeight » Altura Disponível (Sem A Barra De Tarefas) 
 *  screen.colorDepth » Número De Bits Usados Para Cores
 *  screen.pixelDepth » Retorna A Profundidade De Pixel Da Tela
 * »____________LOCATION____________
 *  window.location.href » URL Completa Da Página Atual
 *  window.location.hostname » Nome Do Domínio Da Página (Ex: www.site.com)
 *  window.location.pathname » Caminho Do Arquivo (Ex: /pasta/index.html)
 *  window.location.protocol » Protocolo Usado (Ex: http: ou https:)
 *  window.location.assign() » Redireciona Para Uma Nova URL
 *  window.location.reload() » Recarrega A Página Atual
 * »____________HISTORY____________
 *  window.history.back() » Volta Para A Página Anterior
 *  window.history.forward() » Avança Para A Próxima Página
 * »____________NAVIGATOR____________
 *  window.navigator.cookieEnabled » Retorna True Se Os Cookies Estiverem Ativos
 *  window.navigator.language » Idioma Do Navegador
 *  window.navigator.onLine » Retorna true Se Estiver Online
 *  window.navigator.appName » Nome Do Navegador
 *  window.navigator.appCodeName » Nome Do Código Do Navegador
 *  window.navigator.product » Retorna O Nome Do Motor Do Navegador
 *  window.navigator.appVersion » Versão Do Navegador
 *  window.navigator.userAgent » Retorna O Cabeçalho user-agent Enviado Pelo Navegador Para O Servidor
 *  window.navigator.plataform » Informações Do Navegador E Sistema
 *  window.navigator.javaEnabled() » Retorna True Se Java Estiver Habilitado
 * »____________TIMING____________
 *  window.setTimeout() » Executa Uma Função Após Um Determinado Tempo
 *  window.setInterval() » Executa Uma Função Repetidamente Em Intervalos De Tempo
 *  clearTimeout() » Cancela Uma Função Agendada Pelo setTimeout()
 *  clearInterval() » Cancela Uma Função Repetitiva Do setInterval()
 * 
 */